#include "libft.h"

int	ft_trimend(char const *s)
{
	int	i;

	i = ft_strlen(s);
	while (s[i] == ' ' || s[i] == '\n' || s[i] == '\t' || s[i] == '\0')
		i--;
	return (i);
}
